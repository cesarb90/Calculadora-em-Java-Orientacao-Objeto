package br.com.calculadora;

public class Calculadora {

	// atributos
	float a;
	float b;
	float res;

// construtores

	public Calculadora() {
	}

	// metodos

	// getandset

	public float getA() {
		return a;
	}

	public void setA(float a) {
		this.a = a;
	}

	public float getB() {
		return b;
	}

	public void setB(float b) {
		this.b = b;
	}

	public float getRes() {
		return res;
	}

	public void setRes(float res) {
		this.res = res;
	}
}